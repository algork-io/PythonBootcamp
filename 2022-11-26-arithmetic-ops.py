# operators
# +, -, *, /, 
# // (integer division), 
# % (modulo/modulus divison)
# ** (power operator)
count_of_pages = 100
sets = 11
number_of_pages_in_each_set = count_of_pages//sets
remaining_pages = count_of_pages%sets
print(number_of_pages_in_each_set)
print(remaining_pages)

number_1 = 20
raise_to = 2
result = number_1 ** raise_to
type_of_result = type(result)
print(result)
print(type_of_result)

